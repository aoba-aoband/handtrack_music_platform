    def _build_sensitivity_section(self, parent):
        sec = self._section(parent, "🤏 PINCH SENSITIVITY")
        self._row(sec, "Pinch Min",
                  lambda p: self._slider(p, "pinch_min", 0.0, 0.15, resolution=0.005))
        self._row(sec, "Pinch Max",
                  lambda p: self._slider(p, "pinch_max", 0.10, 0.50, resolution=0.005))
        self._row(sec, "Amp Max",
                  lambda p: self._slider(p, "amp_max", 0.0, 1.0, resolution=0.01))
        self._row(sec, "Gamma",
                  lambda p: self._slider(p, "gamma", 0.5, 4.0, resolution=0.1, digits=2))
        self._row(sec, "Master Volume",
                  lambda p: self._slider(p, "master_amp", 0.0, 2.0, resolution=0.05))

    def _build_smoothing_section(self, parent):
        sec = self._section(parent, "〜 SMOOTHING")
        self._row(sec, "Freq Smooth α",
                  lambda p: self._slider(p, "alpha_freq", 0.01, 0.5, resolution=0.01))
        self._row(sec, "Amp Smooth α",
                  lambda p: self._slider(p, "alpha_amp", 0.01, 1.0, resolution=0.01))

    def _build_log_panel(self, parent):
        tk.Label(parent, text="LOG", font=FONT_HEAD,
                 fg=C["text_dim"], bg=C["bg"]).pack(anchor="w", pady=(0, 4))
        self.log_box = scrolledtext.ScrolledText(
            parent, font=FONT_MONO, bg=C["entry_bg"], fg=C["text"],
            insertbackground=C["accent"], relief="flat",
            state="disabled", wrap="word",
            highlightbackground=C["border"], highlightthickness=1)
        self.log_box.pack(fill="both", expand=True)

    def _build_buttons(self, parent):
        btn_frame = tk.Frame(parent, bg=C["bg"])
        btn_frame.pack(fill="x", pady=(10, 0))

        self.run_btn = tk.Button(
            btn_frame, text="▶  RUN", font=("Courier New", 11, "bold"),
            bg=C["btn_run"], fg="#000", activebackground=C["btn_hover"],
            relief="flat", cursor="hand2", height=2,
            command=self._run)
        self.run_btn.pack(fill="x", pady=(0, 6))

        self.stop_btn = tk.Button(
            btn_frame, text="■  STOP", font=("Courier New", 11, "bold"),
            bg=C["btn_stop"], fg="#fff", activebackground="#ff8888",
            relief="flat", cursor="hand2", height=2,
            state="disabled", command=self._stop)
        self.stop_btn.pack(fill="x", pady=(0, 6))

        tk.Button(btn_frame, text="💾 Save Settings", font=FONT_UI,
                  bg=C["border"], fg=C["text"], relief="flat",
                  cursor="hand2", command=self._save).pack(fill="x", pady=(0, 4))

        tk.Button(btn_frame, text="↺ Reset Defaults", font=FONT_UI,
                  bg=C["border"], fg=C["text_dim"], relief="flat",
                  cursor="hand2", command=self._reset).pack(fill="x")

    # ── 値の適用 / 収集 ──────────────────────
    def _apply_cfg_to_vars(self):
        for key, var in self._vars.items():
            val = self.cfg.get(key)
            if val is None:
                continue
            try:
                if isinstance(var, tk.StringVar):
                    var.set(str(val))
                elif isinstance(var, (tk.DoubleVar, tk.IntVar)):
                    var.set(val)
            except Exception:
                pass

    def _collect_cfg(self) -> dict:
        cfg = dict(self.cfg)
        for key, var in self._vars.items():
            try:
                raw = var.get()
                if key in ("samplerate", "blocksize"):
                    cfg[key] = int(raw)
                elif key in ("scale_name", "custom_pcs"):
                    cfg[key] = raw
                elif isinstance(var, tk.IntVar):
                    cfg[key] = int(raw)
                else:
                    cfg[key] = float(raw)
            except Exception:
                pass
        # デバイスインデックスをコンボから取得
        sel = self.device_combo.current()
        if sel >= 0 and sel < len(self._device_list):
            cfg["device_index"] = self._device_list[sel][0]
        return cfg

    # ── デバイスリスト更新 ────────────────────
    def _refresh_device_list(self):
        devs = get_audio_devices()
        self._device_list = devs
        labels = [f"[{i}] {name}" for i, name in devs]
        self.device_combo["values"] = labels if labels else ["(no output devices found)"]

        # 設定値と一致するデバイスを選択
        target = self.cfg.get("device_index", 8)
        for idx, (dev_i, _) in enumerate(devs):
            if dev_i == target:
                self.device_combo.current(idx)
                return
        if labels:
            self.device_combo.current(0)

    # ── 実行 / 停止 ──────────────────────────
    def _run(self):
        if self.process and self.process.poll() is None:
            messagebox.showinfo("Running", "Already running.")
            return

        cfg = self._collect_cfg()
        script_code = generate_script(cfg)

        # 一時スクリプトに書き出し
        tmp_path = os.path.join(os.path.dirname(__file__), "_theremin_run.py")
        with open(tmp_path, "w", encoding="utf-8") as f:
            f.write(script_code)

        self._log(f"▶ Starting theremin (scale={cfg['scale_name']}, "
                  f"device={cfg['device_index']}, sr={cfg['samplerate']})\n")

        try:
            self.process = subprocess.Popen(
                [sys.executable, tmp_path],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                text=True,
                encoding="utf-8",
                errors="replace",
            )
        except Exception as e:
            self._log(f"[ERROR] {e}\n")
            return

        self._set_running(True)
        threading.Thread(target=self._read_output, daemon=True).start()

    def _stop(self):
        if self.process:
            try:
                self.process.terminate()
            except Exception:
                pass
        self._set_running(False)
        self._log("■ Stopped.\n")

    def _read_output(self):
        try:
            for line in self.process.stdout:
                self._log(line)
        except Exception:
            pass
        self.process.wait()
        rc = self.process.returncode
        self._log(f"[Process exited: {rc}]\n")
        self.root.after(0, lambda: self._set_running(False))

    def _set_running(self, running: bool):
        if running:
            self.run_btn.config(state="disabled")
            self.stop_btn.config(state="normal")
            self.status_dot.config(fg=C["accent3"])
            self.status_lbl.config(text="RUNNING", fg=C["accent3"])
        else:
            self.run_btn.config(state="normal")
            self.stop_btn.config(state="disabled")
            self.status_dot.config(fg=C["text_dim"])
            self.status_lbl.config(text="STOPPED", fg=C["text_dim"])

    # ── ログ ──────────────────────────────────
    def _log(self, text: str):
        def _inner():
            self.log_box.config(state="normal")
            self.log_box.insert("end", text)
            self.log_box.see("end")
            self._log_lines += text.count("\n")
            if self._log_lines > 500:
                self.log_box.delete("1.0", "200.0")
                self._log_lines -= 200
            self.log_box.config(state="disabled")
        self.root.after(0, _inner)

    # ── 保存 / リセット ──────────────────────
    def _save(self):
        cfg = self._collect_cfg()
        save_settings(cfg)
        self.cfg = cfg
        self._log("💾 Settings saved.\n")

    def _reset(self):
        if messagebox.askyesno("Reset", "Restore all defaults?"):
            self.cfg = dict(DEFAULTS)
            self._apply_cfg_to_vars()
            self._refresh_device_list()
            self._log("↺ Reset to defaults.\n")

    def _on_close(self):
        self._stop()
        self.root.destroy()


