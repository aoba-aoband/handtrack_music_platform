def generate_script(cfg: dict) -> str:
    """設定から実行用Pythonスクリプト文字列を生成する"""

    if cfg["scale_name"] == "Custom":
        try:
            pcs = [int(x.strip()) for x in cfg["custom_pcs"].split(",") if x.strip()]
        except Exception:
            pcs = DEFAULTS["custom_pcs"]
    else:
        pcs = PRESET_SCALES.get(cfg["scale_name"], [4, 11, 1])

    # 強調ルート音（先頭音）
    root_pc = pcs[0] if pcs else 0

    script = f'''# Auto-generated by Theremin GUI
import cv2
import mediapipe as mp
import math
import numpy as np
import os

os.environ["SD_ENABLE_ASIO"] = "1"

import sounddevice as sd

# ── Pitch helpers ──
A4 = 440.0

def midi_from_freq(f):
    return 69 + 12 * math.log2(f / A4)

def freq_from_midi(m):
    return A4 * (2 ** ((m - 69) / 12))

def y_to_freq(y_norm, y_low={cfg["y_low"]}, y_high={cfg["y_high"]}, f_min={cfg["f_min"]}, octaves={cfg["octaves"]}):
    y = min(max(y_norm, y_high), y_low)
    u = (y_low - y) / (y_low - y_high)
    return f_min * (2 ** (u * octaves))

def freq_to_y(freq, y_low={cfg["y_low"]}, y_high={cfg["y_high"]}, f_min={cfg["f_min"]}, octaves={cfg["octaves"]}):
    freq = max(freq, 1e-6)
    u = math.log2(freq / f_min) / octaves
    u = min(max(u, 0.0), 1.0)
    return y_low - (y_low - y_high) * u

# ── Scale ──
SCALE_PCS = {pcs}

NOTE_NAMES = {{
    0:"C",1:"C#",2:"D",3:"D#",4:"E",5:"F",
    6:"F#",7:"G",8:"G#",9:"A",10:"A#",11:"B",
}}

def midi_to_note_name(m):
    m = int(round(m))
    return f"{{NOTE_NAMES[m % 12]}}{{(m // 12) - 1}}"

def build_scale_midis(midi_min, midi_max, pitch_classes):
    out = []
    for m in range(int(math.floor(midi_min)) - 12, int(math.ceil(midi_max)) + 13):
        if (m % 12) in pitch_classes:
            out.append(m)
    return out

def quantize_midi_to_scale(midi_value, scale_midis):
    return min(scale_midis, key=lambda m: abs(m - midi_value))

# ── Volume (pinch) ──
def clamp(x, lo, hi):
    return lo if x < lo else hi if x > hi else x

def pinch_to_amp(pinch_norm,
                 pinch_min={cfg["pinch_min"]}, pinch_max={cfg["pinch_max"]},
                 amp_min={cfg["amp_min"]},   amp_max={cfg["amp_max"]},
                 gamma={cfg["gamma"]}):
    p = clamp(pinch_norm, pinch_min, pinch_max)
    u = (p - pinch_min) / (pinch_max - pinch_min)
    u = u ** gamma
    return amp_min + (amp_max - amp_min) * u

# ── Audio ──
SR      = {int(cfg["samplerate"])}
BLOCK   = {int(cfg["blocksize"])}
DEVICE  = {int(cfg["device_index"])}

state = {{
    "phase": 0.0,
    "freq": 220.0,
    "amp": 0.0,
    "target_freq": 220.0,
    "target_amp": 0.0,
}}

def audio_callback(outdata, frames, time, status):
    if status:
        print("audio status:", status)
    alpha_f = {cfg["alpha_freq"]}
    alpha_a = {cfg["alpha_amp"]}
    state["freq"] = (1 - alpha_f) * state["freq"] + alpha_f * state["target_freq"]
    state["amp"]  = (1 - alpha_a) * state["amp"]  + alpha_a * state["target_amp"]
    indices = np.arange(frames, dtype=np.float32)
    phases  = state["phase"] + 2.0 * np.pi * state["freq"] * indices / SR
    y = (state["amp"] * np.sin(phases)).astype(np.float32)
    state["phase"] = (phases[-1] + 2.0 * np.pi * state["freq"] / SR) % (2.0 * np.pi)
    outdata[:, 0] = y
    outdata[:, 1] = y

# ── MediaPipe ──
mp_hands = mp.solutions.hands
mp_draw  = mp.solutions.drawing_utils

hands = mp_hands.Hands(
    model_complexity=0,
    max_num_hands=2,
    min_detection_confidence=0.45,
    min_tracking_confidence=0.45,
)

cap = cv2.VideoCapture(0)
cap.set(cv2.CAP_PROP_BUFFERSIZE, 1)
if not cap.isOpened():
    raise RuntimeError("Camera not found / cannot open.")

y_low, y_high = {cfg["y_low"]}, {cfg["y_high"]}
f_min, octaves = {cfg["f_min"]}, {cfg["octaves"]}

midi_min = midi_from_freq(f_min)
midi_max = midi_from_freq(f_min * (2 ** octaves))
SCALE_MIDIS = build_scale_midis(midi_min, midi_max, SCALE_PCS)

def draw_guides(frame):
    h, w, _ = frame.shape
    cv2.rectangle(frame, (0, int(y_high * h)), (w - 1, int(y_low * h)), (200, 200, 200), 1)
    for m in SCALE_MIDIS:
        f  = freq_from_midi(m)
        y  = freq_to_y(f)
        yy = int(y * h)
        if (m % 12) == {root_pc}:
            color, thickness = (0, 200, 255), 2
        else:
            color, thickness = (255, 255, 255), 1
        cv2.line(frame, (0, yy), (w, yy), color, thickness)
        cv2.putText(frame, midi_to_note_name(m), (w - 70, yy - 4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.45, color, 1)

print("Theremin - Scale: {cfg["scale_name"]} {pcs}")
print("Controls: q=quit, m=mute/unmute, [ ] = volume down/up")

muted     = False
master_amp = {cfg["master_amp"]}

stream = sd.OutputStream(
    device=DEVICE, samplerate=SR, blocksize=BLOCK,
    channels=2, dtype="float32", latency="low",
    callback=audio_callback,
)
stream.start()

try:
    while True:
        ret, frame = cap.read()
        if not ret:
            break

        frame = cv2.flip(frame, 1)
        rgb   = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        res   = hands.process(rgb)

        draw_guides(frame)

        pitch_found = False
        vol_found   = False
        target_amp_from_left = 0.0

        if res.multi_hand_landmarks:
            for hand_lm in res.multi_hand_landmarks:
                lm = hand_lm.landmark
                mp_draw.draw_landmarks(frame, hand_lm, mp_hands.HAND_CONNECTIONS)
                hand_center_x = lm[0].x

                if hand_center_x > 0.5:
                    idx    = lm[mp_hands.HandLandmark.INDEX_FINGER_TIP]
                    f_cont = y_to_freq(idx.y)
                    m_cont = midi_from_freq(max(f_cont, 1e-6))
                    m_q    = quantize_midi_to_scale(m_cont, SCALE_MIDIS)
                    f_q    = freq_from_midi(m_q)
                    state["target_freq"] = f_q
                    pitch_found = True
                    h2, w2, _ = frame.shape
                    yy = int(idx.y * h2)
                    cv2.line(frame, (0, yy), (w2, yy), (0, 255, 0), 2)
                    cv2.putText(frame,
                        f"RIGHT(pitch): {{midi_to_note_name(m_q)}}  {{f_q:.1f}}Hz",
                        (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 255, 0), 2)
                else:
                    th    = lm[mp_hands.HandLandmark.THUMB_TIP]
                    idx   = lm[mp_hands.HandLandmark.INDEX_FINGER_TIP]
                    pinch = math.hypot(th.x - idx.x, th.y - idx.y)
                    target_amp_from_left = pinch_to_amp(pinch) * master_amp
                    vol_found = True
                    cv2.putText(frame,
                        f"LEFT(vol): amp={{target_amp_from_left:.2f}}",
                        (10, 60), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 0), 2)

        if muted:
            state["target_amp"] = 0.0
        else:
            state["target_amp"] = target_amp_from_left if (pitch_found and vol_found) else 0.0

        if not pitch_found:
            cv2.putText(frame, "RIGHT: no hand (pitch)",
                (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 0, 255), 2)
        if not vol_found:
            cv2.putText(frame, "LEFT: no hand (volume)",
                (10, 60), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 0, 255), 2)

        cv2.imshow("Theremin - {cfg["scale_name"]}", frame)
        key = cv2.waitKey(1) & 0xFF

        if key == ord("q"):
            break
        elif key == ord("m"):
            muted = not muted
        elif key == ord("["):
            master_amp = max(0.0, master_amp - 0.05)
        elif key == ord("]"):
            master_amp = min(2.0, master_amp + 0.05)

finally:
    stream.stop()
    stream.close()
    cap.release()
    cv2.destroyAllWindows()
    hands.close()
'''
    return script


